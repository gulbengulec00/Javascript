

// Alınan üç sayıdan en büyüğünü bulma


let a = Number(prompt("Birinci sayıyı giriniz:"));
let b = Number(prompt("İkinci sayıyı giriniz:"));
let c = Number(prompt("Üçüncü sayıyı giriniz:"));


if(a>b && a>c){
    alert("En büyük sayı a")
}
else if(b>a && b>c){
    alert("En büyük sayı b")
}
else if(c>a && c>b){
    alert("En büyük sayı c")
}